# Function to check if two 3D axis-aligned boxes intersect
function intersects_3d(boxA, boxB)
    (Axmin, Aymin, Azmin, Axmax, Aymax, Azmax) = boxA
    (Bxmin, Bymin, Bzmin, Bxmax, Bymax, Bzmax) = boxB

    # Overlap in X?
    if Axmin > Bxmax || Bxmin > Axmax
        return false
    end
    # Overlap in Y?
    if Aymin > Bymax || Bymin > Aymax
        return false
    end
    # Overlap in Z?
    if Azmin > Bzmax || Bzmin > Azmax
        return false
    end

    return true
end

